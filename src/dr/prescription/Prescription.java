    //GET PATIENT INFO IN PRESCRIPTION
    private void p_patientIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_p_patientIdKeyPressed
        cp.patientInfo(p_patientId.getText());
        try{
            while(cp.rs.next()){
                p_patientName.setText(cp.rs.getString("p_Name"));
                
                //For Patient Gender
                if(cp.rs.getString("p_Gender").equals("Male") || cp.rs.getString("p_Gender").equals("Female") || cp.rs.getString("p_Gender").equals("Other")){
                    p_patientGender.removeAllItems();
                    p_patientGender.addItem(cp.rs.getString("p_Gender"));
                }
                else{
                    p_patientGender.removeAllItems();
                    p_patientGender.addItem("-Select-");
                    p_patientGender.addItem("Male");
                    p_patientGender.addItem("Female");
                    p_patientGender.addItem("Other");
                }
                
                //For Patient Blood
                if(cp.rs.getString("p_Blood").equals("A+") || cp.rs.getString("p_Blood").equals("A-") || cp.rs.getString("p_Blood").equals("B+") || cp.rs.getString("p_Blood").equals("B-") || cp.rs.getString("p_Blood").equals("AB+") || cp.rs.getString("p_Blood").equals("AB-") || cp.rs.getString("p_Blood").equals("O+") || cp.rs.getString("p_Blood").equals("O-")){
                    p_patientBlood.removeAllItems();
                    p_patientBlood.addItem(cp.rs.getString("p_Blood"));
                }
                else{
                    p_patientBlood.removeAllItems();
                    p_patientBlood.addItem("-Select-");
                    p_patientBlood.addItem("A+");
                    p_patientBlood.addItem("A-");
                    p_patientBlood.addItem("B+");
                    p_patientBlood.addItem("B-");
                    p_patientBlood.addItem("AB+");
                    p_patientBlood.addItem("AB-");
                    p_patientBlood.addItem("O+");
                    p_patientBlood.addItem("O-");
                }
                
                //For Patient Status
                p_patientStatus.removeAllItems();
                p_patientStatus.addItem(cp.rs.getString("p_Status"));
                
                p_patientAge.setText(cp.rs.getString("p_Age"));
                p_patientMajorCom.setText(cp.rs.getString("p_Mcomplaints"));
                p_patientOtherCom.setText(cp.rs.getString("p_Ocomplaints"));
                
                //View Medicine To the Prescription
                cv.viewMedicineToPrescription(p_patientId.getText(), table_prescription);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error Message:" +ex);
        }
    }//GEN-LAST:event_p_patientIdKeyPressed

